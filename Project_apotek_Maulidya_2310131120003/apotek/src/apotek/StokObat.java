    // Tampilkan data ke tabel
    private void tampilkan_data() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("ID Obat");
        model.addColumn("Nama Obat");
        model.addColumn("Jenis Obat");
        model.addColumn("Harga");
        model.addColumn("Stok");
        model.addColumn("Diskon");

        try {
            String sql = "SELECT id_obat, nama_obat, jenis_obat, harga, stok, diskon FROM stok_obat";
            Connection conn = ConfigDB.config();
            PreparedStatement pstm = conn.prepareStatement(sql);
            ResultSet res = pstm.executeQuery();

            int no = 1;
            while (res.next()) {
                model.addRow(new Object[]{
                    no++, res.getString("id_obat"), res.getString("nama_obat"),
                    res.getString("jenis_obat"), res.getString("harga"),
                    res.getString("stok"), res.getString("diskon")
                });
            }
            tblBarang.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

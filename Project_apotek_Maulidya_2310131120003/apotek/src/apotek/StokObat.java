    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
    // Ambil data dari form
    String id_obat = txtIdObat.getText().trim();
    String nama = txtNama.getText().trim();
    String jenis_obat = txtJenis.getText().trim();
    String hargaStr = txtHarga.getText().trim();
    String stokStr = txtStok.getText().trim();
    String diskonStr = txtDiskon.getText().trim();

    // Validasi input tidak boleh kosong
    if (id_obat.isEmpty() || nama.isEmpty() || jenis_obat.isEmpty() || hargaStr.isEmpty() || stokStr.isEmpty() || diskonStr.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
        return; // Hentikan proses jika ada field kosong
    }

    // Konversi String ke Integer
    int harga = Integer.parseInt(hargaStr);
    int stok = Integer.parseInt(stokStr);
    int diskon = Integer.parseInt(diskonStr);

    // Query UPDATE tanpa SET id_obat
    String sql = "UPDATE stok_obat SET nama_obat = ?, jenis_obat = ?, harga = ?, stok = ?, diskon = ? WHERE id_obat = ?";
    java.sql.Connection conn = ConfigDB.config(); // Pastikan ConfigDB.config() mengembalikan koneksi yang valid
    java.sql.PreparedStatement pstm = conn.prepareStatement(sql);

    // Set parameter untuk query (6 parameter)
    pstm.setString(1, nama);          // nama_obat
    pstm.setString(2, jenis_obat);    // jenis_obat
    pstm.setInt(3, harga);            // harga
    pstm.setInt(4, stok);             // stok
    pstm.setInt(5, diskon);           // diskon
    pstm.setString(6, id_obat);       // WHERE id_obat

    // Eksekusi query
    pstm.executeUpdate();

    // Tampilkan pesan berhasil
    JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
    tampilkan_data();  // Refresh data di tabel
    kosongkan_form();  // Kosongkan form input
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Input harus berupa angka pada kolom Harga, Stok, dan Diskon!");
} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Gagal mengubah data: " + e.getMessage());
    e.printStackTrace();
}


    }//GEN-LAST:event_btnEditActionPerformed


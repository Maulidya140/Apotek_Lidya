    private void tampilkan_data() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("No.");
    model.addColumn("Nama Obat");
    model.addColumn("Harga Obat");
    model.addColumn("Stok Obat");
    
    try {
        String sql = "SELECT id_obat, nama_obat, harga_obat, stok_barang FROM penjualan";
        java.sql.Connection conn = (Connection) ConfigDB.config();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString("id_obat"),
                res.getString("nama_obat"),
                res.getString("harga_obat"),
                res.getString("stok_barang"),
            });
        }
        tblPembayaran.setModel(model);
    } catch (SQLException e) {
        System.out.println("Error: " + e.getMessage());
    }
}
    

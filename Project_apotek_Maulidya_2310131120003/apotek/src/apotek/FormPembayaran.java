    private void btnBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBeliActionPerformed
           // TODO add your handling code here:
        try {
        int jumlah_dibeli = Integer.parseInt(fieldJumlah.getText());
        int uang_masuk = Integer.parseInt(fieldBayar.getText());
        int total = (int) total_harga;

        if (stokBarang >= jumlah_dibeli) {
            if (uang_masuk >= total) {
                java.sql.Connection coon = (Connection) ConfigDB.config();

                // Update stok barang
                String sql = "UPDATE penjualan SET stok_barang = ? WHERE id_obat = ?";
                java.sql.PreparedStatement pstm = coon.prepareStatement(sql);
                pstm.setInt(1, stokBarang - jumlah_dibeli);
                pstm.setInt(2, id_obat);
                pstm.executeUpdate();

                // Hitung uang kembalian
                uang_masuk -= total;
                fieldBayar.setText(Integer.toString(uang_masuk));

                tampilkan_data();
                kosongkan_data();
                JOptionPane.showMessageDialog(null, "Sukses!");
            } else {
                JOptionPane.showMessageDialog(null, "Saldo tidak mencukupi!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Stok barang tidak cukup!");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!");
    }
    }//GEN-LAST:event_btnBeliActionPerformed

    private void tblPembayaranMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPembayaranMouseClicked
        // TODO add your handling code here:
        int baris = tblPembayaran.rowAtPoint(evt.getPoint());
    
        // Ambil data dari setiap kolom pada baris yang diklik
        String id = tblPembayaran.getValueAt(baris, 0).toString(); // Kolom ID Obat
        id_obat = Integer.parseInt(id); // Simpan ID ke variabel id_barang

        String obat = tblPembayaran.getValueAt(baris, 1).toString(); // Kolom Nama Obat
        txtNamaObat.setText(obat); // Tampilkan Nama Obat

        String stokString = tblPembayaran.getValueAt(baris, 3).toString(); // Kolom Stok Barang
        stokBarang = Integer.parseInt(stokString); // Simpan Stok ke variabel stokBarang
        txtStok.setText(stokString); // Tampilkan Stok

        String hargaString = tblPembayaran.getValueAt(baris, 2).toString(); // Kolom Harga Obat
        harga_obat = Integer.parseInt(hargaString); // Simpan Harga ke variabel harga_barang 
        txtHarga.setText("Rp. " + harga_obat);
        // Set field jumlah default ke 1
        fieldJumlah.setText("1");

    }//GEN-LAST:event_tblPembayaranMouseClicked


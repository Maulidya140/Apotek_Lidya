    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        boolean promo = false;

        int jumlah_dibeli = Integer.parseInt(fieldJumlah.getText());
        int uang_masuk = Integer.parseInt(fieldBayar.getText());

        if(jumlah_dibeli >= 0 && stokBarang != 0){
            if(fieldKodeDiskon.getText().equals("sehat")){
                promo = true;
            }

            total_harga = harga_obat * jumlah_dibeli;

            txtHarga.setText("Rp. " + total_harga);
            
            uang_keluar = uang_masuk - total_harga;
            
            txtKembalian.setText("Rp. " + uang_keluar);
            

            if(promo){
                txtPotonganHarga.setText("RP. " + total_harga*0.20);
                total_harga = total_harga - total_harga*0.20;
                uang_keluar = uang_masuk - total_harga;
                txtKembalian.setText("Rp. " + uang_keluar);
            }
            txtTotalHarga.setText("Rp. " + total_harga);
        }
    }//GEN-LAST:event_btnHitungActionPerformed

